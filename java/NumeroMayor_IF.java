
public class NumeroMayor_IF {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        if (a > b) {
            System.out.println("a es mayor");
            a++;
            System.out.println("El valor de a es: " + a);
        } 
        else 
        {
            System.out.println("b es mayor");
            b++;
            System.out.println("El valor de b es: " + b);

        }

    }
    
        
   
    
}