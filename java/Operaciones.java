public class Operaciones {
    public static void main(String args[]) {
		//crear todo el odigo una linea de codiggo 
        int a = 21, b = 7; float c = 0;
		int d = 0, e = 0, f = 0;

		String albertoHernandez = "Hola";
		System.out.println(albertoHernandez);

		//Suma
		d = a + b;
		//Resta
		e = a - b;
		//Multiplicacion
		f = a * b;
		//Divicion
		c = a / b;

		System.out.println("El resultado de la suma es: " + d);
		System.out.println("El resultado de la resta es: " + e);
		System.out.println("El resultado de la multiplicacion es: " + f);
		System.out.println("El resultado de la divicion es: " + c);

    }
}